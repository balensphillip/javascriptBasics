
let x = 5;
let y = 3;

//Addition 
console.log('x + y = ', x + y);  // 8

//Subtraction 
console.log('x - y = ', x - y); // 2

//Multiplication 
console.log('x * y = ', x * y); // 15

//Division
console.log('x / y = ', x / y); // 1.6666666666666667

//Remainder
console.log('x % y = ', x % y); // 2

// increment
console.log('++x = ', ++x); // 6

//Increment
console.log('x++ = ', x++); // 6

//Logging the variable to the console
console.log('x = ', x); // 7

//Decrement
console.log('--x = ', --x); // 6

//Decrement
console.log('x-- = ', x--); // 6

//logging to the variable to the console
console.log('x = ', x); // 5

//Exponential
console.log('x ** y =', x ** y); // 125

//Equal to
console.log(2 == 2); // true

//Equality operator
console.log(2 == '2'); // true

//Not Equal to
console.log(3 != 2); // true

//Not Equal to
console.log('hello' != 'Hello'); // true

//Strict equal to
console.log(2 === 2); // true

//Strict Equal to
console.log(2 === '2'); // false

//Strict Not Equal to
console.log(2 !== '2'); // true

//Strict Not Equal to
console.log(2 !== 2); // false